public class TryingMethods {
    // class for doing the assignment
    public static void main(String[] args) {
        
        
        summer();}

    public static void summer() {
        int myCount = 1;
        int sum = 0;
        while (myCount < 1000) {
            myCount += 1;
            sum += myCount;
        }
        System.out.println(sum);
    }
}